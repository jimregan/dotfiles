sub padrect {
	my $rin = shift;
	my $rout;
	if (length($rin) == 16) {
		return $rin;
	} elsif (length($rin) > 16) {
		return $rout; # can't process, return undef
	} else {
		my $diff = (16 - length($rin));
		for (my $i = 0; $i < $diff; $i++) {
			$rout .= "0";
		}
		return $rout . $rin;
	}
}

sub rect64tompreg {
	my $in = shift;
	my $rect = padrect($in);
	return "" if (!defined $rect);
	my $l=hex(substr($rect,0,4)) / 0xffff;
	my $t=hex(substr($rect,4,4)) / 0xffff;
	my $r=(hex(substr($rect,8,4)) / 0xffff) - $l;
	my $b=(hex(substr($rect,12,4)) / 0xffff) - $t;
	return sprintf("%0.7f, %0.7f, %0.7f, %0.7f", $l, $t, $r, $b);
}

sub rect64tomagick {
	my $in = shift;
	my $height = shift;
	my $width = shift;

	my $rect = padrect($in);
	return "" if (!defined $rect);
	my $lf=hex(substr($rect,0,4)) / 0xffff;
	my $tf=hex(substr($rect,4,4)) / 0xffff;
	my $rf=(hex(substr($rect,8,4)) / 0xffff) - $lf;
	my $bf=(hex(substr($rect,12,4)) / 0xffff) - $tf;

	my $l = int($lf * $height);
	my $r = int($rf * $height);
	my $t = int($tf * $width);
	my $b = int($bf * $width);

	return "${r}x${b}+${l}+${t}";
}

sub writefaceimg {
	my $file = shift;
	my $hash = shift;
	my $reg = shift;
	my $id = shift;
	my $height = shift;
	my $width = shift;

	if (! -d "/tmp/faces") {
		warn "Creating /tmp/faces\n";
		mkdir "/tmp/faces";
	}

	my $coords = rect64tomagick($reg, $height, $width);
	if (!$coords) {
		warn "Could not read coordinates: $reg\n";
		return;
	}
	my $im = new Image::Magick;
	my $pic = $im->Read($file);
	if ($pic) {
		warn "Error reading file: $file\n";
		return;
	}
	$pic = $im->Crop(geometry => $coords);
	$pic = $im->Write("/tmp/faces/${id}_${hash}_${reg}.jpg");
	if ($pic) {
		warn "Error writing file: $file\n";
		return;
	}
	
}

sub do_rect {
	my $in = shift;
	my $height = shift;
	my $width = shift;

	my $rect = padrect($in);
	return "" if (!defined $rect);
	my $l=int((hex(substr($rect,0,4)) / 0xffff) * $height);
	my $t=int((hex(substr($rect,4,4)) / 0xffff) * $width);
	my $r=int((hex(substr($rect,8,4)) / 0xffff) * $height);
	my $b=int((hex(substr($rect,12,4)) / 0xffff) * $width);

	if (defined $rdfmode) {
		return "$l,$t $r,$b";
	} else {
		return "$l, $r, $t, $b";
	}
}

