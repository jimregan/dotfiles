		$base = $file;
		$base =~ s/\.picasa\.ini$//;

		my $hash = Config::INI::Reader->read_file("$file");
	
		while (my ($key, $value) = each(%{$hash}) ) {
			dofaces($base, $key, $value);
		}
	}
}

