				print IGNORE "$mpnode <http://ns.microsoft.com/photo/1.2/t/Region#Rectangle> \"" . rect64tompreg($rect) . "\" .\n";
				if ($haveimage) {
					doignore($url, $rect, $curnode, $info->{'ImageWidth'}, $info->{'ImageHeight'});
				}
			} else {

				print FACES "<$url> <http://jibbering.com/vocabs/image/#hasPart> $mpnode .\n";
				print FACES "$mpnode <http://ns.anvo-it.de/AnVoItPicasa/1.0/t/AnVoItPicasaInfo#FaceId> \"$rect\".\n";
				print FACES "$mpnode <http://ns.microsoft.com/photo/1.2/t/Region#Rectangle> \"" . rect64tompreg($rect) . "\" .\n";

				if (exists $furl{$id}) {
					print FACES "$mpnode <http://xmlns.com/foaf/0.1/depicts> <$furl{$id}> .\n";
				} else {
					print FACES "$mpnode <http://xmlns.com/foaf/0.1/depicts> _:pic$id .\n";
				}

				if ($haveimage) {
					writeface($url, $rect, $curnode, $id, $info->{'ImageWidth'}, $info->{'ImageHeight'});
					writefaceimg($fpath, $bnode, $rect, $id, $info->{'ImageWidth'}, $info->{'ImageHeight'});
				}
			}
		}
	}
}

sub doignore {
	my $url = shift;
	my $rect = shift;
	my $bnode = shift;
	my $width = shift;
	my $height = shift;

	print IGNORE "<$url> <http://jibbering.com/vocabs/image/#hasPart> $bnode .\n"; 
	print IGNORE "$bnode <http://www.w3.org/1999/02/22-rdf-syntax-ns#type> <http://jibbering.com/vocabs/image/#Rectangle> .\n";
	if (!defined $width || $width eq '') {
		print BROKEN "$url\n";
		return;
	}
	my $conv = do_rect($rect, $width, $height);
	print IGNORE "$bnode <http://jibbering.com/vocabs/image/#points> \"$conv\" .\n";
}

